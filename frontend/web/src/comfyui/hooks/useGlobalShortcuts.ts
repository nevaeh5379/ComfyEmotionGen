import { useEffect } from "react"
import { toast } from "sonner"
import type { TabId } from "../components/layout/nav-tabs"

interface UseGlobalShortcutsOptions {
  activeTab: TabId
  mobileJobTab?: "editor" | "status" | "list"
  canRun: boolean
  handleRun: () => void
  handleSave: () => void
  handleGalleryRefresh: () => void
  setActiveTab?: (tab: TabId) => void
  toggleShortcuts?: () => void
}

export function useGlobalShortcuts({
  activeTab,
  mobileJobTab,
  canRun,
  handleRun,
  handleSave,
  handleGalleryRefresh,
  setActiveTab,
  toggleShortcuts,
}: UseGlobalShortcutsOptions) {
  useEffect(() => {
    const handleKeyDown = (e: KeyboardEvent) => {
      // Ignore if typing in editable element
      const activeEl = document.activeElement
      const isEditable =
        activeEl &&
        (activeEl.tagName === "INPUT" ||
          activeEl.tagName === "TEXTAREA" ||
          activeEl.tagName === "SELECT" ||
          activeEl.getAttribute("contenteditable") === "true")

      if (e.key === "?") {
        if (isEditable) return
        e.preventDefault()
        toggleShortcuts?.()
        return
      }

      const isMac = navigator.platform.includes("Mac")
      const modifier = isMac ? e.metaKey : e.ctrlKey

      // Ctrl + Shift + 1..6 or Alt + 1..6 вҶ’ Switch tabs
      const isSwitchTabModifier = (e.altKey && !e.ctrlKey && !e.metaKey && !e.shiftKey) || (modifier && e.shiftKey)
      if (isSwitchTabModifier && e.key >= "1" && e.key <= "6") {
        e.preventDefault()
        const tabIndex = parseInt(e.key) - 1
        const tabs: TabId[] = ["jobs", "stats", "gallery", "curation", "generator", "settings"]
        const targetTab = tabs[tabIndex]
        if (targetTab && setActiveTab) {
          setActiveTab(targetTab)
          const tabNames: Record<TabId, string> = {
            jobs: "мһ‘м—…",
            stats: "нҶөкі„",
            gallery: "к°Өлҹ¬лҰ¬",
            curation: "нҒҗл Ҳмқҙм…ҳ",
            generator: "н…ңн”ҢлҰҝ мғқм„ұкё°",
            settings: "м„Өм •",
          }
          toast.info(`'${tabNames[targetTab]}' нғӯмңјлЎң мқҙлҸҷн–ҲмҠөлӢҲлӢӨ.`)
        }
        return
      }

      // Ctrl/Cmd + Enter вҶ’ Run jobs (Jobs tab only)
      if (modifier && e.key === "Enter" && !e.shiftKey) {
        if (activeTab === "jobs") {
          e.preventDefault()
          if (canRun) {
            handleRun()
            toast.success("мһ‘м—…мқ„ мӢӨн–үн–ҲмҠөлӢҲлӢӨ.")
          }
        }
        return
      }

      // Ctrl/Cmd + S вҶ’ Save (Jobs вҶ’ editor tab only)
      if (modifier && e.key === "s" && !e.shiftKey) {
        if (activeTab === "jobs" && mobileJobTab === "editor") {
          e.preventDefault()
          handleSave()
          toast.success("м ҖмһҘлҗҳм—ҲмҠөлӢҲлӢӨ.")
        }
        return
      }

      // Ctrl/Cmd + Shift + R вҶ’ Refresh gallery (Gallery tab only)
      if (modifier && e.shiftKey && e.key === "R") {
        if (activeTab === "gallery") {
          e.preventDefault()
          handleGalleryRefresh()
          toast.success("к°Өлҹ¬лҰ¬лҘј мғҲлЎңкі м№Ён–ҲмҠөлӢҲлӢӨ.")
        }
        return
      }
    }

    window.addEventListener("keydown", handleKeyDown)
    return () => window.removeEventListener("keydown", handleKeyDown)
  }, [
    activeTab,
    mobileJobTab,
    canRun,
    handleRun,
    handleSave,
    handleGalleryRefresh,
    setActiveTab,
    toggleShortcuts,
  ])
}

